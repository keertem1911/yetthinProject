package com.yetthin.web.domain;

public class StockOfGroup {
	private String groupName;
	private String userID;
	private String initMoney;
	private String strategyId;
	private String indexCode;
	private String stockCode;
	private String stockRatio;
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getInitMoney() {
		return initMoney;
	}
	public void setInitMoney(String initMoney) {
		this.initMoney = initMoney;
	}
	public String getStrategyId() {
		return strategyId;
	}
	public void setStrategyId(String strategyId) {
		this.strategyId = strategyId;
	}
	public String getIndexCode() {
		return indexCode;
	}
	public void setIndexCode(String indexCode) {
		this.indexCode = indexCode;
	}
	public String getStockCode() {
		return stockCode;
	}
	public void setStockCode(String stockCode) {
		this.stockCode = stockCode;
	}
	public String getStockRatio() {
		return stockRatio;
	}
	public void setStockRatio(String stockRatio) {
		this.stockRatio = stockRatio;
	}
	
}
